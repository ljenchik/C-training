// Write a library that has multiple function, where each function return the result of different equations. 
// Write a program that tests the library.
int add(int a, int b) {
  return a + b;
}

int subtract(int a, int b) {
  return a - b;
}

int multiply(int a, int b) {
  return a * b;
}

float divide(int a, int b) {
  return (float)a/b;
}